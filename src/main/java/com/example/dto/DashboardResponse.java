package com.example.dto;

public class DashboardResponse {

	private Integer totalEnqrs;
	private Integer openEnqrs;
	private Integer enrollEnqrs;
	private Integer lostEnqrs;
	
	public Integer getTotalEnqrs() {
		return totalEnqrs;
	}
	public void setTotalEnqrs(Integer totalEnqrs) {
		this.totalEnqrs = totalEnqrs;
	}
	public Integer getOpenEnqrs() {
		return openEnqrs;
	}
	public void setOpenEnqrs(Integer openEnqrs) {
		this.openEnqrs = openEnqrs;
	}
	public Integer getEnrollEnqrs() {
		return enrollEnqrs;
	}
	public void setEnrollEnqrs(Integer enrollEnqrs) {
		this.enrollEnqrs = enrollEnqrs;
	}
	public Integer getLostEnqrs() {
		return lostEnqrs;
	}
	public void setLostEnqrs(Integer lostEnqrs) {
		this.lostEnqrs = lostEnqrs;
	}
	
	
	
	
}
